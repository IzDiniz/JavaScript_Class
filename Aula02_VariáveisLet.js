// Let - É usado para declarar variáveis locais em um escopo de bloco, instrução ou expressão.
// usado para deixar a variável guardada em um escopo local, ou seja, dentro de um bloco de código.

let nome = "Iago Diniz";
console.log("Meu nome é", nome);


let nome2;
nome2 = "Iago";
console.log("Meu nome é", nome2);
nome2 = "Enzo"
console.log("Meu nome é", nome2);
